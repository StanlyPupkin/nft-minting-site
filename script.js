const botanixNetwork = {
  chainId: "0xE34", // 3636 in hex
  chainName: "Botanix Testnet",
  rpcUrls: ["https://node.botanixlabs.dev"],
  nativeCurrency: {
    name: "BTC",
    symbol: "BTC",
    decimals: 18,
  },
  blockExplorerUrls: ["https://testnet.botanixscan.io"],
};

async function switchToBotanix() {
  if (window.ethereum) {
    try {
      await window.ethereum.request({
        method: "wallet_addEthereumChain",
        params: [botanixNetwork],
      });
    } catch (error) {
      console.error("Error adding Botanix network:", error);
    }
  }
}

async function connectWallet() {
  if (window.ethereum) {
    try {
      await switchToBotanix();
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      await provider.send("eth_requestAccounts", []);
      const signer = provider.getSigner();
      const address = await signer.getAddress();
      document.getElementById("status").textContent = `Connected: ${address}`;
      document.getElementById("mintNFT").disabled = false;
    } catch (error) {
      console.error("Error connecting wallet:", error);
      document.getElementById("status").textContent = "Failed to connect wallet.";
    }
  } else {
    alert("MetaMask is not installed!");
  }
}

async function mintNFT() {
  if (window.ethereum) {
    try {
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const signer = provider.getSigner();
      const contract = new ethers.Contract(contractAddress, abi, signer);
      const mintPrice = ethers.utils.parseUnits("0.000001", "ether"); // Price in Botanix BTC

      const tx = await contract.mintNFT({ value: mintPrice });
      document.getElementById("status").textContent = "Minting in progress...";
      await tx.wait();
      document.getElementById("status").textContent = `NFT Minted! Transaction Hash: ${tx.hash}`;
    } catch (error) {
      console.error("Error minting NFT:", error);
      document.getElementById("status").textContent = "Minting failed.";
    }
  } else {
    alert("MetaMask is not installed!");
  }
}

document.getElementById("connectWallet").addEventListener("click", connectWallet);
document.getElementById("mintNFT").addEventListener("click", mintNFT);
